// ─── Helper Functions ────────────────────────────────────────────────────────

export function getClientById(id: string) {
  return clients.find(c => c.id === id);
}

export function getRanchById(id: string) {
  return ranches.find(r => r.id === id);
}

export function getBlockById(id: string) {
  return blocks.find(b => b.id === id);
}

export function getRanchesByClient(clientId: string) {
  return ranches.filter(r => r.clientId === clientId);
}

export function getBlocksByRanch(ranchId: string) {
  return blocks.filter(b => b.ranchId === ranchId);
}

export function getBlocksByClient(clientId: string) {
  return blocks.filter(b => b.clientId === clientId);
}

export function getProbesByBlock(blockId: string) {
  return remoteSensingProbes.filter(p => p.blockId === blockId);
}

export function getProbesByRanch(ranchId: string) {
  return remoteSensingProbes.filter(p => p.ranchId === ranchId);
}

export function getMicrobiologyByBlock(blockId: string) {
  return microbiologyReports.filter(r => r.blockId === blockId);
}

export function getSoilChemistryByBlock(blockId: string) {
  return soilChemistryReports.filter(r => r.blockId === blockId);
}

export function getWaterChemistryByRanch(ranchId: string) {
  return waterChemistryReports.filter(r => r.ranchId === ranchId);
}